<?php
/*
 *---------------------------------------------------------------
 * ERROR REPORTING
 *---------------------------------------------------------------
 */

ini_set('display_errors', false);

/*
 *---------------------------------------------------------------
 * TEMPLATE RESOURCES
 *---------------------------------------------------------------
 */

require_once("vendor/autoload.php");

use \Slim\Slim;
use \Hcode\Page;
use \Hcode\PageAdmin;

$app = new Slim();

$app->config('debug', true);

/*
 *---------------------------------------------------------------
 * ROUTES
 *---------------------------------------------------------------
 */
	
$app->get("/", function() {

	$page = new Page();

	$page->setTpl("index");

});

$app->get("/carrinho", function() {

	$page = new Page();

	$page->setTpl("carrinho");

});

/*
 *---------------------------------------------------------------
 * ADMIN ROUTES 
 *---------------------------------------------------------------
 */

$admin = "/admin";

$app->get( $admin , function() {

	$page = new PageAdmin();

	$page->setTpl("index");

});


/*
 *---------------------------------------------------------------
 * TEMPLATE RUN
 *---------------------------------------------------------------
 */


$app->run();


 ?>